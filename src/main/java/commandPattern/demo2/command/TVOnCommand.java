package commandPattern.demo2.command;

import commandPattern.demo2.TV;

public class TVOnCommand implements Command{

    private TV tv;

    public TVOnCommand(TV tv) {
        this.tv = tv;
    }

    @Override
    public void execute() {
        tv.on();
    }

    @Override
    public void undo() {
        tv.off();
    }
}
